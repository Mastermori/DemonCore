from lark import ParseTree
from demonass_parser.dictionarys import registers

def get_subtree(tree: ParseTree, depth: int, index: int = 0):
    subtree = tree
    for i in range(depth-1):
        subtree = subtree.children[0]
    return subtree.children[index]

def register_lookup(register_str) -> int:
    return registers[register_str]

def get_register_bits(register_str) -> int:
    return f'{register_lookup(register_str):05b}'

def two_complement(value: int, length: int):
    bitmask = '1' * length
    if (value < 0):
        temp = int(bin(value)[3:], 2) ^ int(bitmask, 2)
        return f'{temp+1:{length}b}'
    else:
        return f'{value:0{length}b}'

